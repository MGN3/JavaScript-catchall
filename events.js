/*Eventos de ratón:

click: Se dispara cuando se hace clic en un elemento.
mouseover: Se dispara cuando el puntero del ratón entra en un elemento.
mouseout: Se dispara cuando el puntero del ratón sale de un elemento.
mousemove: Se dispara cuando el puntero del ratón se mueve dentro de un elemento.
mousedown: Se dispara cuando se presiona un botón del ratón mientras el puntero está sobre un elemento.
mouseup: Se dispara cuando se suelta un botón del ratón previamente presionado.

Eventos de teclado:
keydown: Se dispara cuando se presiona una tecla.
keyup: Se dispara cuando se suelta una tecla previamente presionada.
keypress: Se dispara cuando se presiona una tecla y se mantiene pulsada.

Eventos de formulario:
submit: Se dispara cuando se envía un formulario.
change: Se dispara cuando el valor de un campo de formulario cambia.
input: Se dispara cuando se introduce o modifica el valor de un campo de formulario.

Eventos de ventana:
load: Se dispara cuando se ha cargado completamente un objeto (como una imagen o un documento).
resize: Se dispara cuando se cambia el tamaño de la ventana del navegador.
scroll: Se dispara cuando se desplaza el contenido de un elemento.
 */

